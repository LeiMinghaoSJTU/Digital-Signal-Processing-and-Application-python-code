# include <graphics.h>
int main()
{initgraph(640,480); 
outtextxy(0,0,"计算机图形学"); //(0,0)为左上角显示文字
line(100,100,300,300); //(100,100)-(300,300)画直线段
circle(200,200,100); //(200,200)为圆心，100为半径画圆
rectangle(100,100,300,300); //(100,100)-(300,300)画矩形
getch( );
cleardevice( ); //清屏
int p[4*2]={300,50,100,396,500,396,300,50}; //多边形顶点坐标数组，头尾重合
drawpoly(4,p); //画封闭图形
xyprintf(100,400,"三角形：(%d,%d)-(%d,%d)-(%d,%d)",p[0],p[1],p[2],p[3],p[4],p[5]); //输出数据
getch( ); closegraph( ); 
return 0; }
